#include <stdio.h>

int main() {

    // This is a comment
    /*
    *   This is
    *   a multiple-line
    *   comment.
    */
    // An escape sequence is a character combination 
    // consisting of a \ followed by a letter or a 
    // combination of digits. They specify actions within 
    // a line or string of text.
    // Examples : "\n" = newline, "\t" = tab
    printf("Hello World !\n");
    printf("My name's Tamir Sakhawat.\n");
    printf("1\t2\t3\t4\n5\t6\t7\t8\t9\t10");
    printf("\"I like Pizza\" - Abraham Lincoln \'probably\'");
    return 0;
}